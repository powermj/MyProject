package com.zhxg.yqzj.service.exception.expert;

public class ConsultationTimesRunOutException extends ExpertException {

	/**
	* 
	*/
	private static final long serialVersionUID = 5642386106945836464L;

	/**
	 * 咨询次数已耗尽
	 */
	protected static final String ERROR_CODE = "003";

	public ConsultationTimesRunOutException() {
		super(ERROR_CODE, "The consultation times have run out.");
	}

	public ConsultationTimesRunOutException(String msg) {
		super(ERROR_CODE, msg);
	}
}

public enum SourceType {

    NEWS("01", "网媒"),
    FORUM("02", "论坛"),
    BLOG("03", "博客"),
    MICROBLOG("04", "微博"),
    FLATMEDIA("05", "报刊"),
    WECHAT("06", "微信"),
    VIDEO("07", "视频"),
    APP("09", "APP"),
    COMMENT("10","评论"),
    OTHER("99", "其他");

    String souceTypeName;
    String type;

    SourceType(String type, String souceTypeName) {
        this.type = type;
        this.souceTypeName = souceTypeName;
    }

    public String getSouceTypeName() {
        return souceTypeName;
    }

    public void setSouceTypeName(String souceTypeName) {
        this.souceTypeName = souceTypeName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}

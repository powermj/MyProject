public class MobileCloudCourseware extends BaseEntity {

    /**
     * 课件自增id
     */
    private Integer id;
    /**
     * 课程id
     */
    private Integer courseId;
    /**
     * 课件标题
     */
    private String coursewareTitle;
    /**
     * 课件视频地址
     */
    private String coursewareVideoUrl;
    /**
     * 课件时长
     */
    private String coursewareVideoLength;
    
    public Integer getId() {
        return id;
    }
    
    public void setId(Integer id) {
        this.id = id;
    }
    
    public Integer getCourseId() {
        return courseId;
    }
    
    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }
    
    public String getCoursewareTitle() {
        return coursewareTitle;
    }
    
    public void setCoursewareTitle(String coursewareTitle) {
        this.coursewareTitle = coursewareTitle;
    }
    
    public String getCoursewareVideoUrl() {
        return coursewareVideoUrl;
    }
    
    public void setCoursewareVideoUrl(String coursewareVideoUrl) {
        this.coursewareVideoUrl = coursewareVideoUrl;
    }

    
    public String getCoursewareVideoLength() {
        return coursewareVideoLength;
    }

    
    public void setCoursewareVideoLength(String coursewareVideoLength) {
        this.coursewareVideoLength = coursewareVideoLength;
    }
    
}
